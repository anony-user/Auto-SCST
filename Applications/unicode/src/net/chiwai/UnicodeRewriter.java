    /**Convert MP3 files in Folder to use Unicode ID3 tags
     * @param Folder Files in Folder will be converted
     * @param Recursive Access the folder recurively
     * @param Encoding Encoding for the source
     * 
     */
    private void ID3Convert (File Folder, boolean Recursive, String Encoding) {
        if (Folder.canRead() && Folder.canWrite()) {
            if (Folder.isFile()) {
                if (Folder.getName().toLowerCase().endsWith(".mp3") ) {             
                    System.out.println ("Converting..." + Folder.getAbsoluteFile());       
                    nbProcessed++;           
                    try {
                        if (ID3Rewriter.convert (Folder, Encoding) == 0) {                                                            
                            nbConverted++;                                                        
                        }                                            
                    } catch (Exception e) {                                                
                        System.err.println ("Error " + e.getMessage());                                                                         
                    }
                }        
            } else {            
                System.out.println (Folder.getAbsoluteFile() + " is a folder");                        
                nbFolder++;                
                if (Recursive == true) {                
                    //Recursivly convert files in this folder
                    try {
                        for (int j = 0; j < Folder.list().length; j++) {                        
                            ID3Convert (Folder.listFiles()[j], Recursive, Encoding);                    
                        }            
                    } catch (Exception e) {
                        System.err.println ("Error " + e.getMessage());
                    }
                }
            }
        } else {
            System.err.println ("Cannot read and write " + Folder.getName());            
        }
    }
   
      
